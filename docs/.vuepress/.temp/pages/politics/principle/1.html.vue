<h2 id="运动和静止" tabindex="-1"><a class="header-anchor" href="#运动和静止" aria-hidden="true">#</a> 运动和静止<Badge text="选择题" type="tip" /></h2>
<p><code v-pre>静止的概念</code>：静止是物质运动在一定条件下的稳定状态，包括空间位置和根本性质暂时未变这样两种运动的特殊状态。
运动和静止的关系：对立统一</p>
<p><code v-pre>运动和静止相互区别</code>：运动的绝对性体现了物质运动的变动性、无条件性；静止的相对性体现了物质运动的稳定性、有条件性。</p>
<p><code v-pre>运动和静止相互联系</code>：运动和静止相互依赖、相互渗透、相互包含，“动中有静，静中有动”。无条件的绝对运动和有条件的相对静止构成了事物的矛盾运动。</p>
<p><code v-pre>两种错误的观点</code>：夸大静止，否定运动将导致形而上学；夸大运动，否定静止将导致诡辩论。</p>

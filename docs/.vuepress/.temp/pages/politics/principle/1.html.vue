<p><code v-pre>物质运动和时空的关系</code>：不可分割。时空是物质运动的时空，物质运动是时空中的<s>物质运动</s>。时间和空间是物质运动的<s>存在形式</s>。</p>
<p><code v-pre>时空的特点</code>：物质运动与时间和空间的不可分割证明了时间和空间的<em>客观性</em>。具体物质形态的时空是有限的，而整个物质世界的时空是无限的；物质运动时间和空间的客观实在性是<em>绝对的</em>，物质运动时间和空间的具体特性是<em>相对的</em>。物质、运动、时间、空间具有内在的<em>统一性</em>。从根本上说，世界上除了运动的物质，什么也没有。</p>

<h2 id="毛泽东思想活的灵魂" tabindex="-1"><a class="header-anchor" href="#毛泽东思想活的灵魂" aria-hidden="true">#</a> 毛泽东思想活的灵魂<Badge text="选择题" type="tip" /></h2>
<p>1981年党的十一届六中全会通过的《中国共产党中央委员会关于建国以来党的若干历史问题的决议》（以下简称《历史决议》）指出：贯穿于毛泽东思想各个组成部分的立场、观点和方法，是毛泽东思想的活的灵魂，它们有三个基本方面，即实事求是、群众路线、独立自主。</p>
<p>实事求是，就是一切从实际出发，理论联系实际，坚持在实践中检验真理和发展真理。</p>
<p>群众路线，就是一切为了群众，一切依靠群众，从群众中来，到群众中去，把党的正确主张变为群众的自觉行动</p>
<p>独立自主，就是坚持独立思考，走自己的路，就是坚定不移地维护民族独立、捍卫国家主权，把立足点放在依靠自己力量的基础上，同时积极争取外援，开展国际经济文化交流，学习外国一切对我们有益的先进事物。</p>

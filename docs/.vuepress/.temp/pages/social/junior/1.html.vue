<h4 id="促进服务对象正常生活" tabindex="-1"><a class="header-anchor" href="#促进服务对象正常生活" aria-hidden="true">#</a> 促进服务对象正常生活</h4>
<p>对生活上有困难的人给予必要的帮助是社会工作的重要任务，社会工作的功能则是通过此服务来恢复和促进困难群体、有需要群体的正常生活。</p>
<h4 id="恢复弱化的功能" tabindex="-1"><a class="header-anchor" href="#恢复弱化的功能" aria-hidden="true">#</a> 恢复弱化的功能</h4>
<p>对于贫弱群体来说，由于生理、心理及社会方面的原因，他们的某些方面的功能可能衰退了。社会工作通过开展活动、提供服务，可以恢复他们的自信，也可以促使他们参与更广泛的社会生活，这就是在恢复和强化他们的社会功能，以促进他们的社会融人。</p>
<h4 id="促进人的发展" tabindex="-1"><a class="header-anchor" href="#促进人的发展" aria-hidden="true">#</a> 促进人的发展</h4>
<p>人是有潜能的，人的潜能充分发挥有利于他们的正常生活和自我实现。社会工作通过开展多种服务，可以促进他们被压抑的潜能的发挥，为他们的发展创造条件。</p>
<h4 id="促进人与社会环境的相互适应" tabindex="-1"><a class="header-anchor" href="#促进人与社会环境的相互适应" aria-hidden="true">#</a> 促进人与社会环境的相互适应</h4>
<p>社会工作分析问题的基本观点是“人在环境中&quot;，认为人是生活在社会环境之中的，人与社会环境是相互依存的。社会应该为人的正常生活、成长与发展提供适宜的条件，而个人则有责任发挥自己的能力、减少对社会的依赖、经营好自己的生活、为社会发展作贡献。</p>

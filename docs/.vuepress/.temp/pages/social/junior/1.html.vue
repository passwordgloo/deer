<h2 id="社会工作功能" tabindex="-1"><a class="header-anchor" href="#社会工作功能" aria-hidden="true">#</a> 社会工作功能</h2>
<h3 id="功能" tabindex="-1"><a class="header-anchor" href="#功能" aria-hidden="true">#</a> 功能</h3>
<h4 id="含义" tabindex="-1"><a class="header-anchor" href="#含义" aria-hidden="true">#</a> 含义</h4>
<p>功能是指在一个系统中某个部分所发挥的作用，即某一部分的存在和变化对整体及其他部分所发挥的影响。</p>

<p>==教育小组==的宗旨在于，通过帮助小组组员学习新知识、新方法，或补充相关知识不足，促使成员改变其原来对于自己问题的不正确看法及解决方式，从而实现小组组员的发展目标。如青少年礼仪学习小组。</p>
